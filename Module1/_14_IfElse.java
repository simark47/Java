public class _14_IfElse {
        public static void main(String args[]) {
                int age = 16;

                if (age < 13) {
                        System.out.println("Child");
                } else if (age >= 13 && age <= 19) {
                        System.out.println("Teen");
                } else {
                        System.out.println("Adult");
                }

        }
}
